#include<iostream>
#include<cmath>
using namespace std;
//LAB1Q2 
int main()
{
    float decimal1,decimal2;//Division operation will be performed and decimals are required.
    cout<<"Enter decimal number 1: ";
    cin>>decimal1;
    cout<<"Enter decimal number 2: ";
    cin>>decimal2;
    cout<<"decimal1+decimal2= "<<decimal1+decimal2<<endl;
    cout<<"decimal1-decimal2= "<<decimal1-decimal2<<endl;
    cout<<"decimal1*decimal2= "<<decimal1*decimal2<<endl;
    cout<<"decimal1/decimal2= "<<decimal1/decimal2<<endl;
    cout<<"sqrt(square root) decimal1= "<<sqrt(decimal1)<<endl;
    return 0;
}